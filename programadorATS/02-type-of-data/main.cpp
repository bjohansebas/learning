#include<iostream>

using namespace std;

int main() {
    // Number
    int number = 15;
    cout<<number<<endl;

    // Float - less
    float decimal_l = 15.85;
    cout<<decimal_l<<endl;

    // Decimal - more
    double decimal_m = 16.4576;
    cout<<decimal_m<<endl;

    // Character
    char word = 'a';
    cout<<word<<endl;

    return 0;
}